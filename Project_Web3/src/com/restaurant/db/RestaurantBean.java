package com.restaurant.db;
public class RestaurantBean {
	private int textnum;
	private String user_id;
	private String user_email;
	private String restaurant_name;
	private String restaurant_address;
	private String restaurant_phonenum;
	
	public int getTextnum() {
		return textnum;
	}
	public void setTextnum(int textnum) {
		this.textnum = textnum;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getRestaurant_name() {
		return restaurant_name;
	}
	public void setRestaurant_name(String restaurant_name) {
		this.restaurant_name = restaurant_name;
	}
	public String getRestaurant_address() {
		return restaurant_address;
	}
	public void setRestaurant_address(String restaurant_address) {
		this.restaurant_address = restaurant_address;
	}
	public String getRestaurant_phonenum() {
		return restaurant_phonenum;
	}
	public void setRestaurant_phonenum(String restaurant_phonenum) {
		this.restaurant_phonenum = restaurant_phonenum;
	}
	
		

}
